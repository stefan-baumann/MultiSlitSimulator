        /// <summary>
        /// Find the color in the library which most closely resembles fits the specified wavelength.
        /// </summary>
        /// <param name="wavelength">The wavelength.</param>
        /// <returns>An approximation to the color of the light of the wavelength.</returns>
        public static RgbColor GetNearestColor(double wavelength)
        {
            double matchingWavelength = VisibleLightSpectrum.WavelengthColorMap.Keys.OrderBy(w => Math.Abs(wavelength - w)).First();
            uint argb = VisibleLightSpectrum.WavelengthColorMap[matchingWavelength];
            double r = ((argb >> 16) & 0xFF) / 255.0;
            double g = ((argb >> 8) & 0xFF) / 255.0;
            double b = (argb & 0xFF) / 255.0;
            return new RgbColor(r, g, b);
        }

